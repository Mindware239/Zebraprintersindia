// Helper function to get translation
export const getTranslation = (key, language = 'en') => {
  if (!key || typeof key !== 'string') {
    return key || '';
  }
  
  const keys = key.split('.');
  let value = translations;
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  return value?.[language] || value?.['en'] || key;
};
